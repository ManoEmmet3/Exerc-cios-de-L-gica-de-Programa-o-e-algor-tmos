"""

 Faça um Programa que peça as 4 notas bimestrais e mostre a média

"""

grade_1 = int(input("Digite a primeira nota : "));
grade_2 = int(input("Digite a segunda nota : "));
grade_3 = int(input("Digite a terceira nota : "));
grade_4 = int(input("Digite a quarta nota : "));


grade_point = (grade_1 + grade_2 + grade_3 + grade_4) /4 ;

print(f"a media da sua nota é {grade_point}");